//! Awareness Mesh — background watcher system for logs, email, code, calendar.
//! Each watcher: poll via Connect sister → parse via Data sister →
//! check against profile beliefs → alert if threshold crossed.
//!
//! Why isn't a sister doing this? This orchestrates ACROSS sisters (Connect
//! for polling, Data for parsing, Memory for context). No single sister owns
//! the awareness pipeline.

use crate::sisters::SistersHandle;
use std::sync::{Mutex, OnceLock};

/// Global awareness state — persists across sessions.
pub static GLOBAL_AWARENESS: OnceLock<Mutex<AwarenessState>> = OnceLock::new();

pub fn awareness_state() -> &'static Mutex<AwarenessState> {
    GLOBAL_AWARENESS.get_or_init(|| Mutex::new(AwarenessState::new()))
}

/// Type of watcher in the mesh.
#[derive(Debug, Clone, PartialEq)]
pub enum WatcherType {
    Log,
    Email,
    Code,
    Calendar,
    Endpoint,
    Custom(String),
}

/// A single watcher configuration.
#[derive(Debug, Clone)]
pub struct Watcher {
    pub name: String,
    pub watcher_type: WatcherType,
    pub target: String,
    pub interval_secs: u64,
    pub threshold: AlertThreshold,
    pub enabled: bool,
}

/// When to fire an alert.
#[derive(Debug, Clone)]
pub struct AlertThreshold {
    pub condition: String,
    pub severity: AlertSeverity,
}

/// Alert severity levels.
#[derive(Debug, Clone, PartialEq, Ord, PartialOrd, Eq)]
pub enum AlertSeverity {
    Info,
    Important,
    Urgent,
}

/// An alert generated by a watcher.
#[derive(Debug, Clone)]
pub struct AwarenessAlert {
    pub watcher_name: String,
    pub severity: AlertSeverity,
    pub title: String,
    pub detail: String,
    pub timestamp: String,
}

/// Accumulated awareness state.
#[derive(Debug)]
pub struct AwarenessState {
    pub alerts: Vec<AwarenessAlert>,
    pub last_sweep: Option<String>,
    pub watchers_active: usize,
}

impl AwarenessState {
    pub fn new() -> Self {
        Self {
            alerts: Vec::new(),
            last_sweep: None,
            watchers_active: 0,
        }
    }

    /// Add an alert to the accumulator.
    pub fn push_alert(&mut self, alert: AwarenessAlert) {
        self.alerts.push(alert);
    }

    /// Drain all alerts (consumed by morning briefing).
    pub fn drain_alerts(&mut self) -> Vec<AwarenessAlert> {
        std::mem::take(&mut self.alerts)
    }

    /// Count pending alerts.
    pub fn pending_count(&self) -> usize {
        self.alerts.len()
    }
}

