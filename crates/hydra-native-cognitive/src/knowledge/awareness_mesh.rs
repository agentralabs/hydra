/// Run a sweep of all watchers. Called from Dream State idle handler.
pub async fn run_sweep(
    watchers: &[Watcher],
    sisters: &SistersHandle,
) -> Vec<AwarenessAlert> {
    let mut alerts = Vec::new();
    let now = chrono::Utc::now().to_rfc3339();

    for watcher in watchers.iter().filter(|w| w.enabled) {
        let result = poll_watcher(watcher, sisters).await;
        if let Some(alert_detail) = result {
            alerts.push(AwarenessAlert {
                watcher_name: watcher.name.clone(),
                severity: watcher.threshold.severity.clone(),
                title: format!("[{}] {}", watcher.name, watcher.threshold.condition),
                detail: alert_detail,
                timestamp: now.clone(),
            });
        }
    }

    // Store alerts in global state
    if let Ok(mut state) = awareness_state().lock() {
        for alert in &alerts {
            state.push_alert(alert.clone());
        }
        state.last_sweep = Some(now);
        state.watchers_active = watchers.iter().filter(|w| w.enabled).count();
    }

    if !alerts.is_empty() {
        eprintln!("[hydra:awareness] Sweep found {} alerts", alerts.len());
    }

    alerts
}

/// Poll a single watcher via Connect sister.
async fn poll_watcher(
    watcher: &Watcher,
    sisters: &SistersHandle,
) -> Option<String> {
    match watcher.watcher_type {
        WatcherType::Endpoint => {
            // Use Connect sister to check endpoint health
            let content = format!("[awareness-poll] endpoint: {}", watcher.target);
            sisters.memory_workspace_add(&content, "awareness-mesh").await;
            // In production: call Connect sister's health check tool
            // For now: record the poll attempt
            None
        }
        WatcherType::Code => {
            let content = format!("[awareness-poll] code: {}", watcher.target);
            sisters.memory_workspace_add(&content, "awareness-mesh").await;
            None
        }
        WatcherType::Log => {
            let content = format!("[awareness-poll] log: {}", watcher.target);
            sisters.memory_workspace_add(&content, "awareness-mesh").await;
            None
        }
        _ => None,
    }
}

/// Get watchers for a profile name.
pub fn watchers_for_profile(profile_name: &str) -> Vec<Watcher> {
    match profile_name {
        "dev" => default_dev_watchers(),
        "devops" | "infra" => default_devops_watchers(),
        _ => Vec::new(),
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_awareness_state() {
        let mut state = AwarenessState::new();
        assert_eq!(state.pending_count(), 0);
        state.push_alert(AwarenessAlert {
            watcher_name: "test".into(),
            severity: AlertSeverity::Info,
            title: "test alert".into(),
            detail: "detail".into(),
            timestamp: "now".into(),
        });
        assert_eq!(state.pending_count(), 1);
        let drained = state.drain_alerts();
        assert_eq!(drained.len(), 1);
        assert_eq!(state.pending_count(), 0);
    }

    #[test]
    fn test_default_watchers() {
        let w = default_dev_watchers();
        assert!(!w.is_empty());
        assert!(w.iter().all(|w| w.enabled));
    }

    #[test]
    fn test_watchers_for_profile() {
        assert!(!watchers_for_profile("dev").is_empty());
        assert!(!watchers_for_profile("devops").is_empty());
        assert!(watchers_for_profile("unknown_profile").is_empty());
    }

    #[test]
    fn test_severity_ordering() {
        assert!(AlertSeverity::Urgent > AlertSeverity::Important);
        assert!(AlertSeverity::Important > AlertSeverity::Info);
    }
}
